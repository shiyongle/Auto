package com.model.userCustomer;

public class UserCustomer implements java.io.Serializable {

	/***
	 *<p>Description: </p>
	 *<p>Company: CPS-TEAM</p> 
	 * @author WANGC
	 * @date 2015-8-11 上午9:58:59
	*/
	private static final long serialVersionUID = -7987027514032744236L;
	private String fid;
	private String fuserid;
	private String fcustomerid;

	public UserCustomer() {
	}

	public UserCustomer(String fid) {
		this.fid = fid;
	}

	public UserCustomer(String fid, String fuserid, String fcustomerid) {
		this.fid = fid;
		this.fuserid = fuserid;
		this.fcustomerid = fcustomerid;
	}

	public String getFid() {
		return this.fid;
	}

	public void setFid(String fid) {
		this.fid = fid;
	}

	public String getFuserid() {
		return this.fuserid;
	}

	public void setFuserid(String fuserid) {
		this.fuserid = fuserid;
	}

	public String getFcustomerid() {
		return this.fcustomerid;
	}

	public void setFcustomerid(String fcustomerid) {
		this.fcustomerid = fcustomerid;
	}

}
